C.....MAGNITUDE DEPENDENCE.................................
      if (mag .le. mhT) then
         term1 = e1T*mechS + e2T*mechN + e3T*mechR +
     1           e4T*(mag-mhT) + e5T*(mag-mhT)**2.0
      else
         term1 = e1T*mechS + e2T*mechN + e3T*mechR +
     1           e6T*(mag-mhT)
      endif

